        /// Specifies the CSS class name for successful login messages.
        /// </summary>
        public const string LoginSucceeded = "loginSucceeded";
        /// <summary>
        /// Specifies the CSS class name for login-failed error messages.
        /// </summary>
        public const string LoginFailed = "loginFailed";
